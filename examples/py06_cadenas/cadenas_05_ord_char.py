# ord() devuelve el valor del punto de código ASCII/UNICODE de un caracter
ch1 = '@'
ch2 = ' '  # Espacio

print(ord(ch1))  # Salida: 64
print(ord(ch2))  # Salida: 32

# chr() devuelve el caracter correspondiente al valor del punto de código ASCII/UNICODE
print(chr(97))  # Salida: a
print(chr(945))  # Salida: b
