# Se pueden acceder a los elementos de la tupla utilizando índices:
mi_tupla = (1, 2.0, "cadena", [3, 4], (5, ), True)

# Debemos accederlos con el subíndice entre []
print(mi_tupla[0])  # Salida: 1
print(mi_tupla[1])  # Salida: 2.0
print(mi_tupla[2])  # Salida: cadena
print(mi_tupla[3])  # Salida: [3, 4]
print(mi_tupla[4])  # Salida: (5,)
print(mi_tupla[5])  # Salida: True
