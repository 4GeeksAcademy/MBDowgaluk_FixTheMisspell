# Se pueden acceder a los elementos de la tupla utilizando índices:
miTupla = (1, 2.0, "cadena", [3, 4], (5, ), True)

# Debemos accederlos con el subíndice entre []
print(miTupla[0])  # salida: 1
print(miTupla[1])  # salida: 2.0
print(miTupla[2])  # salida: cadena
print(miTupla[3])  # salida: [3, 4]
print(miTupla[4])  # salida: (5, )
print(miTupla[5])  # salida: True
