# Para comprobar si una clave existe en un diccionario, se puede emplear la palabra reservada in:
miDiccionario = {
    "fire": "fuego",
    "water": "agua",
    "earth": "tierra",
    "wind": "viento",
}
if "fire" in miDiccionario:
    print("SI")
else:
    print("NO")

